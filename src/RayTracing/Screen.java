package RayTracing;

public class Screen {

	public Vector origin;
	public int width;
	public int height;
	
	public Screen(){
		
	}
	
	public void setOrigin(Vector origin){
		this.origin=origin;
	}
	
	public void setWidth(int width){
		this.width=width;
	}
	
	public void setHeight(int height){
		this.height=height;
	}
}
